
import { createContext, useContext, useEffect, useState } from 'react';
import { User, Session } from '@supabase/supabase-js';
import { supabase } from '@/integrations/supabase/client';
import { toast } from 'sonner';

interface UserProfile {
  id: string;
  user_id: string;
  email: string;
  name: string;
  role: 'admin' | 'client' | 'operator';
  active: boolean;
}

interface AuthContextType {
  user: User | null;
  profile: UserProfile | null;
  session: Session | null;
  loading: boolean;
  signIn: (email: string, password: string) => Promise<{ error: any }>;
  signUp: (email: string, password: string, role?: 'admin' | 'client' | 'operator') => Promise<{ error: any }>;
  signOut: () => Promise<void>;
  isAdmin: boolean;
  isClient: boolean;
  isOperator: boolean;
}

const AuthContext = createContext<AuthContextType | undefined>(undefined);

export function AuthProvider({ children }: { children: React.ReactNode }) {
  const [user, setUser] = useState<User | null>(null);
  const [profile, setProfile] = useState<UserProfile | null>(null);
  const [session, setSession] = useState<Session | null>(null);
  const [loading, setLoading] = useState(true);

  const fetchUserProfile = async (userId: string) => {
    try {
      console.log('üîç Obteniendo perfil para usuario:', userId);
      
      const { data: profileData, error } = await supabase
        .from('user_profiles')
        .select('*')
        .eq('user_id', userId)
        .single();
      
      if (error) {
        console.error('‚ùå Error obteniendo perfil:', error);
        
        // Si no hay perfil, intentar crearlo desde los metadatos del usuario
        if (error.code === 'PGRST116') {
          console.log('üîß Perfil no encontrado, intentando crear...');
          
          const { data: userData } = await supabase.auth.getUser();
          if (userData.user) {
            const userMetadata = userData.user.user_metadata || {};
            const defaultRole = userMetadata.role || 'client';
            const defaultName = userMetadata.name || userData.user.email?.split('@')[0] || 'Usuario';
            
            const { data: newProfile, error: createError } = await supabase
              .from('user_profiles')
              .insert({
                user_id: userId,
                email: userData.user.email || '',
                name: defaultName,
                role: defaultRole,
                active: true
              })
              .select()
              .single();
            
            if (createError) {
              console.error('‚ùå Error creando perfil:', createError);
              return null;
            }
            
            console.log('‚úÖ Perfil creado:', newProfile);
            return newProfile;
          }
        }
        
        return null;
      }

      console.log('‚úÖ Perfil encontrado:', profileData);
      return profileData;
    } catch (err) {
      console.error('‚ùå Error en fetchUserProfile:', err);
      return null;
    }
  };

  useEffect(() => {
    console.log('üîÑ Configurando listener de autenticaci√≥n...');
    
    const { data: { subscription } } = supabase.auth.onAuthStateChange(
      async (event, session) => {
        console.log('üîÑ Estado de auth cambi√≥:', event, session?.user?.email);
        setSession(session);
        setUser(session?.user ?? null);
        
        if (session?.user && event !== 'SIGNED_OUT') {
          const profileData = await fetchUserProfile(session.user.id);
          setProfile(profileData);
        } else {
          setProfile(null);
        }
        
        setLoading(false);
      }
    );

    // Verificar sesi√≥n inicial
    supabase.auth.getSession().then(({ data: { session } }) => {
      console.log('üîç Verificando sesi√≥n inicial:', session?.user?.email);
      
      if (session?.user) {
        setSession(session);
        setUser(session.user);
        fetchUserProfile(session.user.id).then(profileData => {
          setProfile(profileData);
          setLoading(false);
        });
      } else {
        setLoading(false);
      }
    });

    return () => {
      console.log('üîÑ Limpiando subscription de auth');
      subscription.unsubscribe();
    };
  }, []);

  const signUp = async (email: string, password: string, role: 'admin' | 'client' | 'operator' = 'client') => {
    try {
      setLoading(true);
      console.log('üìù Registrando usuario:', email, 'con rol:', role);
      
      const { data, error } = await supabase.auth.signUp({
        email,
        password,
        options: {
          data: {
            role: role,
            name: email.split('@')[0]
          }
        }
      });
      
      if (error) {
        console.error('‚ùå Error de registro:', error);
        toast.error(`Error: ${error.message}`);
        return { error };
      }

      if (data.user) {
        console.log('‚úÖ Registro exitoso:', data.user.email);
        toast.success('¬°Usuario registrado correctamente!');
        return { error: null };
      }
      
      return { error };
    } catch (err) {
      console.error('‚ùå Error en signUp:', err);
      toast.error('Error de conexi√≥n durante el registro');
      return { error: err };
    } finally {
      setLoading(false);
    }
  };

  const signIn = async (email: string, password: string) => {
    try {
      setLoading(true);
      console.log('üîê Iniciando sesi√≥n:', email);
      
      const { data, error } = await supabase.auth.signInWithPassword({
        email,
        password,
      });
      
      if (error) {
        console.error('‚ùå Error de inicio de sesi√≥n:', error);
        toast.error(`Error: ${error.message}`);
        return { error };
      }

      if (data.user) {
        console.log('‚úÖ Inicio de sesi√≥n exitoso:', data.user.email);
        toast.success('¬°Sesi√≥n iniciada correctamente!');
        return { error: null };
      }
      
      return { error };
    } catch (err) {
      console.error('‚ùå Error en signIn:', err);
      toast.error('Error de conexi√≥n');
      return { error: err };
    } finally {
      setLoading(false);
    }
  };

  const signOut = async () => {
    try {
      console.log('üö™ Cerrando sesi√≥n...');
      
      // Limpiar estado primero
      setUser(null);
      setProfile(null);
      setSession(null);
      
      const { error } = await supabase.auth.signOut();
      if (error) {
        console.error('‚ùå Error al cerrar sesi√≥n:', error);
        toast.error('Error al cerrar sesi√≥n');
      } else {
        console.log('‚úÖ Sesi√≥n cerrada correctamente');
        toast.success('Sesi√≥n cerrada');
      }
    } catch (err) {
      console.error('‚ùå Error en signOut:', err);
      toast.error('Error al cerrar sesi√≥n');
    }
  };

  const value: AuthContextType = {
    user,
    profile,
    session,
    loading,
    signIn,
    signUp,
    signOut,
    isAdmin: profile?.role === 'admin',
    isClient: profile?.role === 'client',
    isOperator: profile?.role === 'operator',
  };

  return (
    <AuthContext.Provider value={value}>
      {children}
    </AuthContext.Provider>
  );
}

export function useAuth() {
  const context = useContext(AuthContext);
  if (context === undefined) {
    throw new Error('useAuth debe usarse dentro de un AuthProvider');
  }
  return context;
}
